import Foundation
import UIKit

public func font(_ fontSize: CGFloat) -> UIFont{
    return UIFont.systemFont(ofSize:fontSize)
}

public func boldFont(_ fontSize: CGFloat) -> UIFont{
    return UIFont.boldSystemFont(ofSize:fontSize)
}

extension UIFont{
    
    
}
